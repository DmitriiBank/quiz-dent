function App() {
  return (
      <Router>
        <div className="app">
          <header className="app-header">
            <h1>Стоматологические тесты</h1>
          </header>
          <main>
            <Routes>
              <Route path="/" element={<QuizSelectionPage />} />
              <Route path="/quiz/:quizId" element={<QuizPage quizData={quizData} />} />
            </Routes>
          </main>
        </div>
      </Router>
  );
}
const QuizPage = ({ quizData }) => {
    const { quizId } = useParams();
    const navigate = useNavigate();
    const quiz = quizData[quizId];

    useEffect(() => {
        if (!quiz) {
            const timer = setTimeout(() => navigate('/', { replace: true }), 2000);
            return () => clearTimeout(timer);
        }
    }, [quiz, navigate]);

    if (!quiz) {
        return (
            <div className="quiz-not-found">
                <p>Тест не найден. Перенаправление на главную страницу...</p>
            </div>
        );
    }

    return (
        <div className="quiz-page">
            <h2>{quiz.title}</h2>
            <button className="back-button" onClick={() => navigate('/')}>
                ← Назад к выбору тестов
            </button>
            <QuizApp questions={quiz.questions} />
        </div>
    );
};

export default App;